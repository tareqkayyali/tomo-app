"""
Tomo AI Service — Response Card Models v2

Extends cards.py with the additional card types that the mobile app renders
but were missing Pydantic validation. This file adds 15 card types to bring
the total from 13 → 28 validated types.

The mobile app renders 47 total types (19 display + 28 capsules), but capsule
cards are generated by deterministic code (not LLM), so they don't need
LLM-output validation. The 28 types here cover all LLM-generated cards.

Usage: Import CARD_TYPE_MAP_V2 in format_response to validate every card
before streaming to mobile. Unknown types pass through with a warning.
"""

