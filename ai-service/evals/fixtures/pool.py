def get_by_slug(slug: str) -> EvalAthlete:
    """Return the fixture matching `slug` or raise KeyError."""
    for athlete in EVAL_ATHLETES:
        if athlete["slug"] == slug:
            return athlete
    raise KeyError(f"No eval fixture with slug {slug!r}")


